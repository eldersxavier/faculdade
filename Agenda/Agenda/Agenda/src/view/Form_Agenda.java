        jTNOME.setText(String.valueOf(modelo.getValueAt(linha, 1)));
	jTTELEFONE.setText(String.valueOf(modelo.getValueAt(linha, 2)));
	jTCELULAR.setText(String.valueOf(modelo.getValueAt(linha, 3)));
	jTEMAIL.setText(String.valueOf(modelo.getValueAt(linha, 4)));
	jTENDERECO.setText(String.valueOf(modelo.getValueAt(linha, 5)));
