    public Contatos_java getTxtParaContato(){
        contato = new Contatos_java();
        //jTID.getText()
        int id;
        if(!jTID.getText().equals("") && !jTID.getText().equals(null) && !jTID.getText().equals(" ")){
            id = Integer.parseInt(jTID.getText());
        }else{
            id = 0;
        }
        contato.setId(id);
        contato.setNome(jTNOME.getText());
	contato.setTelefone(jTTELEFONE.getText());
	contato.setCelular(jTCELULAR.getText());
	contato.setEmail(jTEMAIL.getText());
	contato.setEndereco(jTENDERECO.getText());
	contato.setNumero(jTNUMERO.getText());
        contato.setBairro(jTBAIRRO.getText());
        contato.setCidade(jTCIDADE.getText());
	contato.setEstado(jTESTADO.getText());
        contato.setPais(jTPAIS.getText());    
        return contato;
    }
