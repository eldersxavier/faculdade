    public void Adicionar(){        
        dao = new DAO();  
        dao.setAdiciona(getTxtParaContato());
        JOptionPane.showMessageDialog(null, "Contato Adicionado com sucesso!!!");        
        inicioForm();
        int linha = jTabela.getSelectedRow();        
        linha = jTabela.getRowCount();        
        jTabela.setRowSelectionInterval((jTabela.getRowCount()-1), (jTabela.getRowCount()-1));
        TabelaParaTxt();
    }
    public void Editar(){                
        TabelaParaTxt();
        DesbloqueiaTxt();
        jBADICIONAR.setEnabled(true);
        jBSALVAR.setEnabled(true);
        jBADICIONAR.setEnabled(true);
        jBDELETAR.setEnabled(true);        
    }
    public void Salvar(){        
        dao = new DAO();  
        dao.setAtualiza(getTxtParaContato());
        JOptionPane.showMessageDialog(null, "Contato Atualizado com sucesso!!!");  
        inicioForm();
    }
    public void Deletar(){        
                          
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja excluir contato?");
        if (resposta == JOptionPane.YES_OPTION) {
            // verifica se o usuário clicou no botão YES
            dao = new DAO();
            //Deleta contato
            dao.setDeleta(getTxtParaContato());
            JOptionPane.showMessageDialog(null, "Contato excluido com sucesso!!!");            
        } 
        inicioForm();
    }
    
