    //Atualiza a dabela com as dados do banco de dados
    public void AtualizaTabela(){
        limparTabela();
        dao = new DAO();        
        List<Contatos_java> contatos = dao.getLista();
        for(int i = 0; i < (contatos.size()); i++){        
           AddLinhaTabela();           
           modelo.setValueAt(contatos.get(i).getId(), i, 0);           
           modelo.setValueAt(contatos.get(i).getNome(), i, 1);
           modelo.setValueAt(contatos.get(i).getTelefone(), i, 2);
           modelo.setValueAt(contatos.get(i).getCelular(), i, 3);
           modelo.setValueAt(contatos.get(i).getEmail(), i, 4);
           modelo.setValueAt(contatos.get(i).getEndereco(), i, 5);
           modelo.setValueAt(contatos.get(i).getNumero(), i, 6);
           modelo.setValueAt(contatos.get(i).getBairro(), i, 7);
           modelo.setValueAt(contatos.get(i).getCidade(), i, 8);
           modelo.setValueAt(contatos.get(i).getEstado(), i, 9);
           modelo.setValueAt(contatos.get(i).getPais(), i, 10);           
        }
    }
